def butterGraph(T, A, C):
    G = graph.Graph(7, True)
    G.costs = {}
#    G.labels = ["PB", "B", "A", "S", "I", "F", "E"]
    for i in range(7):
        for j in range(7):
            if T[i][j] or C[i][j] or A[i][j]:
                val = T[i][j] + C[i][j] - A[i][j]
                G.addedge(i, j)
                G.costs[(i, j)] = val
    return G

